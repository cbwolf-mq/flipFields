# returns a random vector of length s mod D
def rndVecOdd(s,D): 
  assert (s % 2) == 1, "even s with "+str(s)  
  fromList = range(1,D,2)
  resVec = s*[1]
  for i in range(len(resVec)):
    resVec[i] = random.choice(fromList)  
  return resVec 

# returns a random matrix of size (s x s) mod D
def rndMatOdd(s,D):
  assert (s % 2) == 1, "even s with "+str(s)  
  M = [1]*s
  for i in range(s):
    M[i] = rndVecOdd(s,D)   
  return M  

# generate a lower triangular matrix 
def rndLU_lower(s,D):
  # generate a random lower matrix and modify it layer by layer
  L = rndMatOdd(s,D)
  # set first row
  L[0][0] = 1
  for i in range(1,s): L[0][i] = 0
  # set all other rows
  for layer in range(1,s//2+1):
    layRow = layer*2-1
    L[layRow][layRow+1] = 1
    L[layRow+1][layRow+1] = 1
    for i in range(layRow+2,s):
      L[layRow][i] = 0
      L[layRow+1][i] = 0
  # test if we created the correct matrix
  assert isLU_lower(L,D)     
  return L

# generate an upper triangular matrix 
def rndLU_upper(s,D):
  # generate a random upper matrix and modify it layer by layer
  U = rndMatOdd(s,D)
  for layer in range((s // 2)):
    for i in range(layer*2, layer*2+1):
      for j in range(0,layer*2-1):
        U[i][j] = 0
  assert isLU_upper(U,D)      
  return U

