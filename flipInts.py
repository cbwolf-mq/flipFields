# compute y = Mx for given M, y
# https://en.wikipedia.org/wiki/Iterative_method - iterative method
# https://en.wikipedia.org/wiki/Gauss%E2%80%93Seidel_method - GaussSeidel method
# Input:
#   M: (s x s) matrix
#   y: (s x 1) vector
# Output
#   x: (s x 1) vector
# All computations mod D and s an odd number
def solveMatVec(M,y,D):
  s = len(y)
  assert (s % 2) == 1, "s not odd: "+str(s)
  assert s == len(M), "mat has wrong size: "+str(len(M))

  # check the input
  for i in range(s):
    assert isElem(y[i],D), "y[i] is not an element for y[i]="+str(y[i])+" i="+str(i)
  for i in range(s):
    assert s == len(M[i]), "mat[i] has wrong size: "+str(len(M[i])+" for "+M[i])
    for j in range(s):      
      assert isElem(M[i][j],D), "M[i,j] is not an element for M[i,j]="+str(M[i][j])+" i="+str(i)+", j="+str(j) 

  # use Gaussian elimination on each column
  # stop if we get a zero on the column in the process---matrix not invertible in this case
  for fromRow in range(s):
    swapRow = -1
    if (M[fromRow][fromRow] % 2) == 0: 
      # swap rows
      for swapRow in range(fromRow+1,s):
        if (M[swapRow][fromRow] % 2) != 0: break
      assert swapRow >= 0, "Non-invertible matrix at fromRow="+str(fromRow)+" for M="+str(M)
      tmpRow = M[fromRow]; M[fromRow] = M[swapRow]; M[swapRow] = tmpRow
      tmpElem = y[fromRow]; y[fromRow] = y[swapRow]; y[swapRow] = tmpElem
    assert M[fromRow][fromRow] != 0, "Non-invertible matrix at fromRow="+str(fromRow)+" for M="+str(M)

    invFrom = invMul(M[fromRow][fromRow],D)
    # multiply the current row such that the pivot element is a one
    for curCol in range(fromRow,s):
      M[fromRow][curCol] = invFrom*M[fromRow][curCol] % D
    assert M[fromRow][fromRow] == 1, "Non-one pivot with "+str(M[fromRow][fromRow])  
    y[fromRow] = invFrom*y[fromRow] % D
    # subtract row by row to achieve zero in column fromRow
    for targetRow in range(fromRow+1,s):
      targetFac = M[targetRow][fromRow]
      for j in range(fromRow,s):
        M[targetRow][j] = (M[targetRow][j] - targetFac*M[fromRow][j]) % D
      # intermediate result
      assert M[targetRow][fromRow] == 0, "Non-Zero pivot at targetRow="+str(targetRow)+" fromRow="+str(fromRow)+" for "+str(M)
      y[targetRow] = (y[targetRow] - targetFac*y[fromRow]) % D
    
    # find gcd for each row and divide by it
    for curRow in range(fromRow+1,s):
      allElems = [y[curRow]] + M[curRow]
      allElems = list(filter(lambda x: x != 0, allElems))
      if allElems == []: curGCD = 1
      else: 
        curGCD = allElems[0]
        for i in range(1,len(allElems)): curGCD = math.gcd(curGCD,allElems[i])
      for curCol in range(fromRow+1,s): 
        assert (M[curRow][curCol] % curGCD) == 0, "Not dividible by "+str(curGCD)+" for "+str(M[curRow][curCol]) 
        M[curRow][curCol] //= curGCD
      assert (y[curRow] % curGCD) == 0, "Not dividible by "+str(curGCD)+" for "+str(y[curRow])
      y[curRow] //= curGCD
    
  # reconstruct x for Mx=y
  x = [0]*s  # prepare the result vector
  for fromRow in range(s-1,-1,-1):
    # apply y[fromRow] = Mx for x known so far
    accu = 0
    for curCol in range(fromRow+1,s):
      accu += M[fromRow][curCol]*x[curCol] % D
    assert M[fromRow][fromRow] == 1, "non-one element at "+fromRow+" with "+M[fromRow][fromRow]
    x[fromRow] = (y[fromRow] - accu) % D
#    assert isElem(x[fromRow],D), "non-element x="+str(x[fromRow])
  return x


# compute Ly = b for given L, b
# solve Ly = b 
# L is a generalized lower matrix over the integer carrier
# D is 2^(d+1) for the dimension d of the integer carrier
def solveLU_lower(L,b,D):
  
  # solve Ly = b for some intermediate solution y
  def solveRec_lower(L,b,s,layer,y,D):
    assert isLU_lower(L,D), "L is not generalized lower matrix for L="%str(L)
    assert layer*2 < s, "layer is too high for s for (layer,s)=("+str(layer)+","+str(s)+")"
    rL = layer*2+1  # starts with layer 0
    if rL >= s: return [y.copy()] # we have a solution, we are done
    
    # try all possible values from the input
    Y_out = [] 
    # apply different values of y to the current solution
    for yLower in range(1,D,2):
      ySolFirst = (b[rL] - (yLower*L[rL][rL] + sum(y[i]*L[rL][i] for i in range(rL)))) % D
      ySolSecond =  (b[rL+1] - (yLower*L[rL+1][rL] + sum(y[i]*L[rL+1][i] for i in range(rL)))) % D
      # verify if this is a solution for both equations layer*2, layer*2+1
      # check if we found a new, possible solution
      if ySolFirst == ySolSecond: 
        y[rL] = yLower; y[rL+1] = ySolFirst
        Y_out += solveRec_lower(L,b,s,layer+1,y,D)
    return Y_out
    
  # main method
  s = len(b)
  assert (s % 2) == 1, "s not odd: "+str(s)
  assert s == len(L), "mat L has wrong size: "+str(len(L))

  # check the input
  for i in range(s):
    assert isElem(b[i],D), "b[i] is not an element for b[i]="+str(b[i])+" i="+str(i)
  for i in range(s):
    assert s == len(L[i]), "L[i] has wrong size: "+str(len(L[i])+" for "+L[i])
    
  # check if L is in the right format  
  assert isLU_lower(L,D), "L is not lower form for L="+str(L)

  # solve Ly = b
  y = [1] * s
  # first layer in L
  y[0] = b[0]
  # recursively solve for all layers
  return solveRec_lower(L,b,s,0,y,D)

# compute Ux = y for given U,y
# solve Ux = y 
# U is a generalized upper matrix over the integer carrier
# D is 2^(d+1) for the dimension d of the integer carrier
def solveLU_upper(U,y,D):
  
  # solve Ux = y for some intermediate solution x
  def solveRec_upper(U,y,s,layer,x,D):
    if layer < 0: return [x.copy()] # we have a solution, we are done
    assert isLU_upper(U,D), "U is not generalized upper matrix for U="%str(U)
    assert layer*2 < s, "layer is too high for s for (layer,s)=("+str(layer)+","+str(s)+")"
    rU = layer*2  # starts with layer 0
    
    # try all possible values from the input
    X_out = [] 
    # apply different values of y to the current solution
    for xLower in range(1,D,2):
      x[rU+1] = xLower; 
      xSolFirst = (invMul(U[rU][rU],D)*(y[rU] - sum(U[rU][i]*x[i] for i in range(rU+1,s)))) % D
      xSolSecond =  (invMul(U[rU+1][rU],D)*(y[rU+1] - sum(U[rU+1][i]*x[i] for i in range(rU+1,s)))) % D
      # verify if this is a solution for both equations layer*2, layer*2+1
      # check if we found a new, possible solution
      if xSolFirst == xSolSecond: 
        x[rU+1] = xLower; x[rU] = xSolFirst
        X_out += solveRec_upper(U,y,s,layer-1,x,D)
    return X_out
    
  # main method
  s = len(y)
  assert (s % 2) == 1, "s not odd: "+str(s)
  assert s == len(U), "mat U has wrong size: "+str(len(U))

  # check the input
  for i in range(s):
    assert isElem(y[i],D), "y[i] is not an element for y[i]="+str(y[i])+" i="+str(i)
  for i in range(s):
    assert s == len(U[i]), "U[i] has wrong size: "+str(len(U[i])+" for "+U[i])
    
  # check if U is in the right format  
  assert isLU_upper(U,D), "U is not upper form for U="+str(U)

  # solve Ux = y
  x = [1] * s
  # lowest layer in U
  x[s-1] = (invMul(U[s-1][s-1], D) * y[s-1]) % D
  # recursively solve for all layers
  return solveRec_upper(U, y, s, (s // 2)-1, x, D) 
  
# solves LUx = b for some vector b and a lower and upper, generalized triangular matrix L, U, respectively
# return all possible solutions x as a list of vectors
def solveLU(L,U,b,D):
  s = len(b)
  assert (s % 2) == 1, "s not odd: "+str(s)
  assert s == len(L), "mat L has wrong size: "+str(len(U))

  # check the input
  for i in range(s):
    assert isElem(b[i],D), "b[i] is not an element for b[i]="+str(b[i])+" i="+str(i)
  for i in range(s):
    assert s == len(L[i]), "L[i] has wrong size: "+str(len(L[i])+" for "+L[i])
  for i in range(s):
    assert s == len(U[i]), "U[i] has wrong size: "+str(len(U[i])+" for "+U[i])

  # check if L is in the right format  
  assert isLU_lower(L,D), "L is not lower form for L="+str(L)   
  # check if U is in the right format  
  assert isLU_upper(U,D), "U is not upper form for U="+str(L)
  
  # solve the equation
  Y_out = solveLU_lower(L,b,D)
  X_out = []
  for y in Y_out:
    assert isVec(y,D), "Invalid solution "+str(y)
    X_out += solveLU_upper(U,y,D)
  return X_out  
  
