def isVec(v,D):
  """
  Check if v is a vector in FlipInts generated by D.

  Parameters
  ----------
  v : list
    Vector over FlipInts generated by D.
  D : int
    Generate a FlipInts.

  Returns
  -------
  bool
    True iff all elements of v are in FlipInts of D.
  """
  for a in v: 
    if (isElem(a,D) != True): return False
  return True

 
def isLU_lower(L,D):
  """
  Check if a matrix is of the generalized lower matrix format.

  Parameters
  ----------
  L : list of lists
    Lower triangular matrix .
  D : int
    Generates the corresponding FlipInts with modulus D.

  Returns
  -------
  bool
    True iff L is a suitable generalized lower triangular matrix.
  """
  s = len(L)
  assert (s % 2) == 1, "s is even "+ str(s)
  for layer in range((s // 2) + 1):
    if layer == 0:
      assert L[0][0] == 1, "L[i,j] is not 1 L[i,j]="+str(L[0][0])+" i=0, j=0"
    else: 
      for i in range(layer*2-1, layer*2):
        for j in range(layer*2 + 1) :
          assert isElem(L[i][j],D), "L[i,j] is not an element for L[i,j]="+str(L[i][j])+" i="+str(i)+ " j="+str(j)
          if j == layer*2: assert L[i][j] == 1, "L[i,j] is not 1 but L[i,j]="+str(L[i][j])+" i="+str(i)+ " j="+str(j)
        for j in range(layer*2+1,s):
          assert L[i][j] == 0, "L[i,j] is none-zero for L[i,j]="+str(L[i][j])+" i="+str(i)+ " j="+str(j)
  return True        
  

# check if a matrix is of the generalized upper matrix format
def isLU_upper(U,D):
  """
  Check if a matrix is of the generalized upper matrix format.

  Parameters
  ----------
  U : list of lists
    Lower triangular matrix .
  D : int
    Generates the corresponding FlipInts with modulus D.

  Returns
  -------
  bool
    True iff U is a suitable generalized upper triangular matrix.
  """
  s = len(U)
  assert (s % 2) == 1, "s is even "+ str(s)
  for layer in range((s // 2)):
    for i in range(layer*2, layer*2+1):
      for j in range(layer*2,s):
        assert isElem(U[i][j],D), "U[i,j] is not an element for U[i,j]="+str(U[i][j])+" i="+str(i)+ " j="+str(j)
      for j in range(0,layer*2-1):
        assert U[i][j] == 0, "U[i,j] is none-zero for U[i,j]="+str(U[i][j])+" i="+str(i)+ " j="+str(j)
  assert isElem(U[s-1][s-1],D), "U[i,j] is not an element for U[i,j]="+str(U[i][j])+" i="+str(i)+ " j="+str(j)
  return True


