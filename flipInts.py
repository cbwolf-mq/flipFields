def invMul(a,D):
  """
  Computes the multiplicative inverse using the Extended Euclidian algorithm.

  Parameters
  ----------
  a : int
    element of the corresponding flipInt
  D : int
    Modulus of the flipField.

  Returns
  -------
  t : int
    Integer t s/t a.t = 1 (mod D)
  """
  assert isElem(a,D), "is not an element a=%d for D=%d"%(a,D)
  t = 0; newT = 1; r = D; newR = a
  while newR != 0:
    q = r // newR
    tmp = newT; newT = t - q * newT; t = tmp  
    tmp = newR; newR = r - q*newR; r = tmp

  assert r <= 1, "r is too big: "+str(r)
  if t < 0: t = t + D
  assert isElem(t,D), "t is not an element: "+str(t)
  return t


def invHensel(a,D):
  """
  Computes the multiplicative inverse using Hensel's lifting.
  See for details (German):
  https://hwlang.de/zahlentheorie/hensel-lifting.htm  

  Parameters
  ----------
  a : int
    element of the corresponding flipInt
  D : int
    Modulus of the flipField.

  Returns
  -------
  t : int
    Integer t s/t a.t = 1 (mod D)
  """    
  def invRec(a, k):
    if k == 1: return 1
    D = 2**k
    a = a % D
    r = invRec(a, (k+1)//2)
    return (2-a*r) * r % D
      
   
  assert isElem(a,D), "is not an element a=%d for D=%d"%(a,D)
  k = log2int(D)
  assert 2**k == D, "k invalid for k=%d, D=%d"%(k,D)
  return invRec(a,k)
  

def applyMatVecOdd(M,x,D):
  """
  compute y = Mx for given M, x.
  All computations mod D and s an odd number.
  Input:
    M: (s x s) matrix
    x: (s x 1) vector
  Output
    y: (s x 1) vector

  Parameters
  ----------
  M : list
    Matrix in the form of list of lits of integers.
  x : list
    vector.
  D : int
    Generating number for a flipInt.

  Returns
  -------
  list
    Vector as a list of ints.
  """
  s = len(x)
  assert (s % 2) == 1, "s not odd: "+str(s)
  assert s == len(M), "mat has wrong size: "+str(len(M))

  # check the input
  for i in range(s):
    assert isElem(x[i],D), "x[i] is not an element for x[i]="+str(x[i])+" i="+str(i)

  for i in range(s):
    for j in range(s): 
      assert isElem(M[i][j],D), "M[i,j] is not an element for M[i,j]="+str(M[i][j])+" i="+str(i)+", j="+str(j) 
  
  # compute
  y = [0]*s  # prepare the result vector

  # apply x to M
  for i in range(s):
    curVec = M[i]
    assert s == len(curVec), "curVec has wrong size: "+str(len(curVec))
    accu = 0
    for j in range(s):
      accu += curVec[j] * x[j]
    y[i] = accu % D

  return y


