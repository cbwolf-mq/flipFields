def symIsPoly(p,v,D,relaxed=True):
  """
  Checks if a polynomial string is well-formed, i.e.
  - each term starts with a number
  - all other factors in this term are a variable from v
  - the term closes with "*"
  
  Parameters
  ----------
  p : str
    Polynomial.
  v : set of str
    variables.
  D : int
    Modulus of the flipField.
  relaxed : bool
    Skip check if all factors are elements of the flipInts  

  Returns
  -------
  bool
    True iff p is well-formed regarding v, D
  """
  assert isField(D), "no field for D=%d"%D
  
  # check if the input is valid
  if p[-1] != "*": return False
    
  # split by terms
  terms = p.split("+")  
  for t in terms:
    facs = t.split("*")
    if facs[-1] != "": return False
    if not(facs[0].isdigit()): return False
    if not(relaxed) and not(isElem(int(facs[0]),D)): return False
    for f in facs[1:-1]:
      if not(f in v): return False
  return True 


def symPolyIsOdd(p,D):
  """
  Returns if the sum of the factors of the polynomial are odd.
  Assuming all input variables are odd, the output will be odd, too.

  Parameters
  ----------
  p : str
    Polynomial as a string.
  D : int
    Generator of the flipInts.

  Returns
  -------
  bool
    True iff the sum of the factors is odd.
  """
  assert isField(D), "no field for D=%d"%D
  
  # check if the input is valid
  if p[-1] != "*": return False
    
  # split by terms, get the factors
  terms = p.split("+") 
  sumFac = 0
  for t in terms:
    facs = t.split("*")
    if facs[-1] != "": return False
    if not(facs[0].isdigit()): return False
    sumFac += int(facs[0])
  return (sumFac % 2) == 1


def symIsLinear(p,v,D):
  """
  Verifies if p is a valid polynomial and also of degree 1.

  Parameters
  ----------
  p : str
    Polynomial.
  v : set of str
    variables.
  D : int
    Modulus of the flipField.

  Returns
  -------
  bool
    True iff the input polynomial is linear.
  """
  assert symIsPoly(p,v,D), "Not a polynomial with "+p
  return functools.reduce(lambda a,b: a and b, [ s.count("*") == 2 for s in p.split("+") ] )


def symAllVars(inEq,D):
  """
  Returns all variables for a given equation.

  Parameters
  ----------
  inEq : str
    Equation.

  Returns
  -------
  set
    set of all variables in this equation.
  """
  outSet = set()
  if inEq == "": return outSet
  for t in inEq.split("+"):
    for f in t.split("*"):
      if (len(f) > 0) and not(f.isdigit()): outSet.add(f)
  assert symIsPoly(inEq,outSet,D), "Wrong equation for "+inEq
  return outSet    


def symSetEqs(eqs,v,var2val,D):
  """
  For a given list of equations, replace one variable by an integer value.

  Parameters
  ----------
  eqs : list
    Equations as list of strings.
  v : set
    set of variables (as strings).
  var2val : dict
    variable to a definite value.
  D : int
    fieldInt.

  Returns
  -------
  list
    list of strings. Equations after replacing a variable.
  """
  # check if each equation is well-formed
  for eq in eqs:
    assert symIsPoly(eq,v,D), "Problem Eq  " + eq 
  assert (len(var2val.keys())) == 1, "Too many variables with " +str(var2val) 
  curKey = list(var2val.keys())[0]
  curVar = var2val[curKey]
  assert str(curVar).isdigit(), "Non-digit value for "+str(var2val)+" and !%s!"%(curVar)
  targetVar = "*" + str(curKey) + "*"
  # if applicable, replace each variable in var2val by its value
  for i in range(len(eqs)):
    pos = eqs[i].find(targetVar)
    if pos < 0: continue
    # split the equation
    terms = eqs[i].split("+")
    outList = []
    for t in terms:
      pos = t.find(targetVar)
      if pos < 0: 
        outList.append(t)
        continue
      t = t.replace(targetVar,"*")
      # only get the constant term
      monConst = t.split("*",1)
      assert monConst[0].isdigit()
      fac = int(monConst[0]) * var2val[curKey] % D
      # make sure we have a non-zero term
      if fac == 0: continue
      outStr = str(fac) + "*"
      outStr += monConst[1]
      outList.append(outStr)
    eqs[i] = "+".join(outList) 
  # check the output
  for eq in eqs:
    assert symIsPoly(eq,v,D), "Problem Eq 2: " + eq   
  return eqs    


def symSetEqsPoly(eqs,v,var2poly,D):
  """
  Replaces a given variable by a polynomial. Multiplies out and collects terms in the process.
  The result is a valid polynomial in the given set of variables.

  Parameters
  ----------
  eqs : list
    List of str, each of them is a valid polynomial.
  v : set
    Set of variables that form the polynomial.
  var2poly : dict
    New polynomial for a variable. Only one variable is allowed, i.e. the dictionary only has one key.
  D : int
    Generating integer for the corresponding flipInts.

  Returns
  -------
  TYPE
    DESCRIPTION.
  """
  assert isField(D), "Wrong flipInts with "+str(D)
  for eq in eqs:
    assert symIsPoly(eq,v,D), "Problem Eq  " + eq 
  assert (len(var2poly.keys())) == 1, "Too many variables with " +str(var2poly) 
  curKey = list(var2poly.keys())[0]
  curVar = var2poly[curKey]
  
  assert (var2poly[curKey] == "") or (symIsPoly(var2poly[curKey],v,D)), "Non polynomial value for "+str(var2poly)+" and !%s!"%(curVar)
  targetVar = "*" + str(curKey) + "*"  
  newTerms = var2poly[curKey].split("+")
  if var2poly[curKey] == "": newTerms = ["0*"]
  for i in range(len(eqs)):
    pos = eqs[i].find(targetVar)
    if pos < 0: continue
    # split the equation
    terms = eqs[i].split("+")
    outList = []
    for t in terms:
      pos = t.find(targetVar)
      if pos < 0: 
        outList.append(t)
        continue
      for newTerm in newTerms:
        newT = t.replace(targetVar,"*"+newTerm)
        newT = symSortTerms(newT, v, D)
        if newT != "0*": outList.append(newT)
    newEq = "+".join(outList)  
    eqs[i] = symAddUp(newEq,D)
  while "" in eqs: eqs.remove("")
  # check the output
  for eq in eqs:
    assert symIsPoly(eq,v,D,relaxed=True), "Problem Eq 2: " + eq   
  return eqs    


def symSortTerms(inEq,v,D):
  """
  For a given polynomial, sorts all terms and also multiplies out factors.

  Parameters
  ----------
  inEq : str
    Input polynomial.
  v : set
    Set of all variables in the input polynomial.
  D : int
    Number generating the corresponding flipInts.

  Returns
  -------
  str
    Sorted and multiplied polynomial.
  """
  assert isField(D), "Invalid flipInts with "+str(D)
  outEq = ""
  for t in inEq.split("+"):
    allVars = t.split("*")
    allVars.remove("")
    f = 1
    for a in allVars:
      if not(a.isdigit()): continue
      f *= int(a)
    f %= D  
    allVars = filter(lambda a: not(a.isdigit()),allVars)
    allVars = sorted(allVars)  
    if outEq != "": outEq += "+"
    outEq += str(f)+"*"+"*".join(allVars)
    if len(allVars) > 0: outEq += "*"
  return outEq  


def symPolyMult(leftPrefix, leftCnt, rightPrefix, rightCnt, D):
  """
  Multiply two polynomials in a given number of variables each.

  Parameters
  ----------
  leftPrefix : str
    Variable of the left factor
  leftCnt : int
    Maximal degree of the left polynomial.
  rightPrefix : str
    Variable of the right factor.
  rightCnt : int
    Maximal degree of the right polynomial.
  D : int
    Generating number for the flipInt  

  Returns
  -------
  list
    List of strings, result of this multiplication.
  """
  outList = []
  for k in range(leftCnt+rightCnt-1):
    outList.append("")
  for i in range(leftCnt):
    for j in range(rightCnt):
      if outList[i+j] != "": outList[i+j] += "+"
      outList[i+j] += "1*" + leftPrefix + str(i) + "*" + rightPrefix + str(j) + "*"
  v = set([leftPrefix + str(i) for i in range(leftCnt)] + [rightPrefix + str(j) for j in range(rightCnt)])    
  for curEq in outList:    
    assert symIsPoly(curEq,v,D), "Problem Eq  " + curEq 
  return outList


def symMulPolyPoly(left, right, D):
  """
  Symbolic multiplication of two polynomials.

  Parameters
  ----------
  left : str
    Polynomial.
  right : str
    Polynomial.
  D : int
    generating number for the flipInt.

  Returns
  -------
  str
    (left * right) % D. All terms are alphabetically sorted and added up.
  """
  assert isField(D), "Invalid flipInts with "+str(D)
  v = symAllVars(left,D).union(symAllVars(right,D))
  assert symIsPoly(left,v,D), "Problem left " + left 
  assert symIsPoly(right,v,D), "Problem right " + right
  leftTerms = left.split("+")
  rightTerms= right.split("+")
  # multiply term by term
  resTerms = ""
  for l in leftTerms:
    facLeft,l = l.split("*",1)
    for r in rightTerms:
      facRight,r = r.split("*",1)
      fac = (int(facLeft) * int(facRight)) % D
      tmpTerm = str(fac) + "*" + l + r
      # collect and rewrite
      resTerms = resTerms + "+" + tmpTerm if resTerms != "" else tmpTerm
  resTerms = symSortTerms(resTerms,v,D)
  resTerms = symAddUp(resTerms,D)
  return resTerms    


def symSubtractTerm(eqStr, subStr, D):
  """
  Subtract subStr from the current equation.

  Parameters
  ----------
  eqStr : str
    Polynomial for the current equation.
  subStr : str
    Term to be subtracted.
  D : int
    Current FlipInts.

  Returns
  -------
  str
    New polynomial.
  """
  subSplit = subStr.split('*',1)
  subFac = int(subSplit[0]); subTerm = subSplit[1]
  allTerms = eqStr.split('+')
  outEq = ""
  isIn = False
  termList = []
  for curTerm in allTerms:
    if curTerm == "": continue
    fullSplit = curTerm.split('*',1)
    fac = int(fullSplit[0]); term = fullSplit[1]
    if subTerm == term:
      isIn = True
      newFac = (D + fac - subFac) % D
      if newFac != 0: termList.append(str(newFac) + "*" + subTerm)
    else: termList.append(curTerm)
  if isIn == False: 
    newFac = (D - subFac) % D; 
    if outEq != "": outEq += "+"
    termList.append(str(newFac) + "*" + subTerm)
  outEq = "+".join(termList)  
  return outEq
  

def symAddUp(inEq,D):
  """
  Adds up all terms that share the same variables. Assumes all terms are well-ordered.

  Parameters
  ----------
  inEq : str
    input equation.
  D : int
    Generating number for the flipInts.

  Returns
  -------
  set
    Output equation.
  """
  v = symAllVars(inEq, D)
  if inEq == "": return ""
  assert symIsPoly(inEq,v,D), "Problem In Eq " + inEq 
  outDict = {}
  for t in inEq.split("+"):
    fac,allVars = t.split("*",1)
    if not(allVars in outDict): outDict[allVars] = 0
    outDict[allVars] = (outDict[allVars] + int(fac)) % D
  # delete zero equations
  for curVar in list(outDict.keys()):
    if outDict[curVar] == 0: del outDict[curVar]
  # generate output
  outEq = "+".join([ str(outDict[v])+"*"+v for v in sorted(outDict.keys()) ])
  # delete 0 terms
  assert symIsPoly(inEq,v,D), "Problem Out Eq " + outEq 
  return outEq


def symMultFacPoly(f,p,v,D):
  """
  Multiplies a given polynomial p by an integer factor f. 

  Parameters
  ----------
  f : int
    Factor.
  p : str
    Polynomial.
  v : set
    Set of all variables.
  D : int
    Modul generating the flipInts.

  Returns
  -------
  str
    Polynomial.
  """
  assert symIsPoly(p,v,D), "Problem Eq " + p
  assert isElem(f, D)
  outStr = ""
  for t in p.split("+"):
    fac,rest = t.split("*",1)
    fac = (int(fac)*f) % D
    if outStr != "": outStr += "+"
    outStr += str(fac) + "*" + rest
  return outStr  
  

def symRewriteEqsLin(leftPrefix, leftCnt, rightPrefix, rightCnt, rightSet, D):
  """
  Removes all closed equations, i.e. equations that have coefficients that add up to an even number.
  All remaining equations are active, i.e. their coefficients add up to an odd number. These produce odd output.

  Parameters
  ----------
  leftPrefix : str
    Prefix for the variables in the polynomial in the left term.
  leftCnt : int
    Number of variables for the left term in the polynomial.
  rightPrefix : str
    Prefix for the variables in the polynomial in the right term.
  rightCnt : int
    Number of variables for the right term in the polynomial.
  rightSet : dict
    Dictionary for the variable in the right factor that are set in the result.
  D : int
    Generating number for the flipInts.

  Returns
  -------
  eqs : list
    Equations in form of polynomials. The output variable is implicit.
  fullReplace : dict
    Variables that have been replaced by polynomials.
  """
  assert isinstance(rightSet,dict), "No dict for "+str(rightSet)
  # generate the system of equations by multiplying left and right variables
  eqs = symPolyMult(leftPrefix, leftCnt, rightPrefix, rightCnt, D)
  # assign random values to the rhs
  allVars = set([ leftPrefix + str(i) for i in range(leftCnt) ] + [ rightPrefix + str(j) for j in range(rightCnt)])
  fullReplace = {}
  for curVar in [ rightPrefix + str(j) for j in range(rightCnt)]:
    assert curVar in rightSet, "Variable does not exist in rightSet for "+curVar
    eqs = symSetEqs(eqs, allVars, {curVar: rightSet[curVar]} , D)
  
  # simplify the system, eliminate closed equations
  done = False
  while (not done):
    done = True
    # check if some polynomial has only one variable - this means the variable is zero
    # check if we have closed equations
    while len(eqs) != 0:
      # check if all equations are linear
      for j in range(len(eqs)): 
        assert symIsLinear(eqs[j],allVars,D), "Non-linear equation for "+eqs[j]
      # find a closed equation with minimal number of terms  
      i = -1; foundEq = -1; foundCnt = 10**100; foundLen = 10**100
      while True:
        i += 1    
        if i >= len(eqs): break
        if symPolyIsOdd(eqs[i-1],D): continue # active equation
        
        eqLen = len(eqs[i])
        cnt = eqs[i-1].count("+")
        if (cnt < foundCnt) or ((cnt == foundCnt) and (eqLen < foundLen)): foundCnt = cnt; foundEq = i-1; foundLen = eqLen  
      if foundEq == -1: break  # no closed equation      
      # remove one variable, make sure leading term is 1
      if eqs[foundEq].count("+") >= 1: t,r = eqs[foundEq].split("+",1)
      else: t = eqs[foundEq]; r = ""
      
      f,v = t.split("*",1)
      assert f.isdigit(), "Wrong number with "+f
      
      f = int(f); 
      if (f % 2) == 1:
        f = (D -f) % D; f = invMul(f, D)
      else:
        # we have an even factor here---split into two odd factors
        r += ("" if r=="" else "+") + (str((f-1) % D))+"*"+v
        f = 1
      repVar = v.replace("*","")
      if r != "": r = symMultFacPoly(f, r, allVars, D)
      fullReplace[repVar] = r
      # remove var/Eqs
      del eqs[foundEq]
      # set the variable accordingly
      eqs = symSetEqsPoly(eqs,allVars,{repVar: fullReplace[repVar]},D)
              
      done = False
    return eqs,fullReplace

