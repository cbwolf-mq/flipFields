# returns A(X). B(X)for two polynomials
def mulPoly(A,B,D):
  assert isPoly(A,D), "A not valid with "+str(A)
  assert isPoly(B,D), "B not valid with "+str(B)
  R = {}  # prepare the result
  # multiply AB
  for a in A.keys():
    for b in B.keys():
      new = a+b
      if not(new in R): R[new] = 0
      R[new] += A[a]*B[b]
  # reduce R    
  for k in R.keys():
    R[k] = R[k] % D    
  return R
  
# for some given polynomial A and target degree deg,
# itentify the coefficients so we can find some polynomial B s/t A.B is a polynomial
# over the odd ring mod D
def mulPolyFindBCoeffs(A,deg,D):
  assert isPoly(A, D), "Error mulPolyFindRndB - no odd poly for "+str(A)
  allCoeffs = {}
  # perform multiplication on the level of X
  for a in A.keys():
    for b in range(deg+1):
      new = a+b
      if not(new in allCoeffs): allCoeffs[new] = 0
      allCoeffs[new] += 1
  # identify degrees that are even
  zeroCoeffs = {}
  for ac in allCoeffs:
    if (allCoeffs[ac]) % 2 == 0: zeroCoeffs[ac] = allCoeffs[ac]
  # similar coeffs
  similarCoeffs = {}
  for a in A.keys():
    for b in range(deg+1):
      if a+b in zeroCoeffs: 
        if not(a+b) in similarCoeffs: similarCoeffs[a+b] = []
        similarCoeffs[a+b].append([a,b])
  return similarCoeffs      
      

